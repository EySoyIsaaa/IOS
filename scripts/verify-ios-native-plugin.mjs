import { readFileSync, existsSync } from 'node:fs';
import { join } from 'node:path';

const root = process.cwd();
const pbxprojPath = join(root, 'ios/App/App.xcodeproj/project.pbxproj');
const infoPlistPath = join(root, 'ios/App/App/Info.plist');
const wrapperPath = join(root, 'client/src/native/iosNativeAudio.ts');
const podfilePath = join(root, 'ios/App/Podfile');
const capacitorConfigPath = join(root, 'ios/App/App/capacitor.config.json');
const pluginPackagePath = join(root, 'plugins/epicenter-native/package.json');
const pluginPodspecPath = join(root, 'plugins/epicenter-native/EpicenterNative.podspec');
const pluginSwiftPath = join(root, 'plugins/epicenter-native/ios/Sources/EpicenterNativePlugin/EpicenterNativePlugin.swift');

const restoredSourceCopies = [
  'Plugins/EpicenterNativePlugin.swift',
  'NativeAudio/NativeAudioModels.swift',
  'NativeAudio/NativeLibraryDatabase.swift',
  'NativeAudio/NativeTrackImporter.swift',
  'NativeAudio/NativeTrackRepository.swift',
  'NativeAudio/NativeAudioSessionManager.swift',
  'NativeAudio/NativeAudioEngine.swift',
  'NativeAudio/NativeQueueManager.swift',
  'NativeAudio/NativePlaybackController.swift',
  'NativeAudio/NowPlayingManager.swift',
  'NativeAudio/RemoteCommandManager.swift',
  'DSP/AudioLimiter.swift',
  'DSP/EQ31BandProcessor.swift',
  'DSP/ReverbProcessor.swift',
  'DSP/EpicenterDSPBridge.h',
  'DSP/EpicenterDSPBridge.mm',
  'DSP/EpicenterDSPCore.hpp',
  'DSP/EpicenterDSPCore.cpp',
];

const pluginPodSources = [
  'EpicenterNativePlugin.swift',
  'NativeAudioModels.swift',
  'NativeLibraryDatabase.swift',
  'NativeTrackImporter.swift',
  'NativeTrackRepository.swift',
  'NativeAudioSessionManager.swift',
  'NativeAudioEngine.swift',
  'NativeQueueManager.swift',
  'NativePlaybackController.swift',
  'NowPlayingManager.swift',
  'RemoteCommandManager.swift',
  'AudioLimiter.swift',
  'EQ31BandProcessor.swift',
  'ReverbProcessor.swift',
];

const requiredPluginMethods = [
  'importTracks',
  'getLibraryPage',
  'getTrack',
  'deleteTrack',
  'setQueue',
  'play',
  'pause',
  'stop',
  'seek',
  'next',
  'previous',
  'getPlaybackState',
];

const fail = (message) => {
  console.error(`❌ ${message}`);
  process.exitCode = 1;
};

for (const path of [pbxprojPath, infoPlistPath, wrapperPath, podfilePath, capacitorConfigPath, pluginPackagePath, pluginPodspecPath, pluginSwiftPath]) {
  if (!existsSync(path)) {
    fail(`Missing required file: ${path}`);
  }
}

if (process.exitCode) process.exit(process.exitCode);

const pbxproj = readFileSync(pbxprojPath, 'utf8');
const infoPlist = readFileSync(infoPlistPath, 'utf8');
const wrapper = readFileSync(wrapperPath, 'utf8');
const podfile = readFileSync(podfilePath, 'utf8');
const capacitorConfig = readFileSync(capacitorConfigPath, 'utf8');
const pluginPackage = readFileSync(pluginPackagePath, 'utf8');
const pluginPodspec = readFileSync(pluginPodspecPath, 'utf8');
const plugin = readFileSync(pluginSwiftPath, 'utf8');

for (const source of restoredSourceCopies) {
  if (!existsSync(join(root, 'ios/App/App', source))) {
    fail(`Missing restored native source copy: ios/App/App/${source}`);
  }
}

for (const source of pluginPodSources) {
  if (!existsSync(join(root, 'plugins/epicenter-native/ios/Sources/EpicenterNativePlugin', source))) {
    fail(`Missing compiled plugin pod source: plugins/epicenter-native/ios/Sources/EpicenterNativePlugin/${source}`);
  }
}

if (pbxproj.includes('EpicenterNativePlugin.swift') || pbxproj.includes('NativeAudio/')) {
  fail('project.pbxproj contains manual native source references; keep native compilation in the local Capacitor pod instead.');
}

if (!wrapper.includes("registerPlugin<EpicenterNativePlugin>('EpicenterNative')")) {
  fail('TypeScript wrapper is not registered as EpicenterNative');
}

if (!pluginPackage.includes('"@epicenter/native"') || !pluginPackage.includes('"capacitor"')) {
  fail('Local plugin package is missing Capacitor metadata');
}

if (!pluginPodspec.includes("s.name = 'EpicenterNative'") || !pluginPodspec.includes("s.dependency 'Capacitor'")) {
  fail('EpicenterNative podspec is missing name or Capacitor dependency');
}

if (!podfile.includes("pod 'EpicenterNative'")) {
  fail('Podfile does not include the EpicenterNative pod generated by Capacitor sync');
}

if (!capacitorConfig.includes('"EpicenterNativePlugin"')) {
  fail('capacitor.config.json packageClassList does not include EpicenterNativePlugin');
}

if (!plugin.includes('@objc(EpicenterNativePlugin)')) {
  fail('EpicenterNativePlugin.swift is missing @objc(EpicenterNativePlugin)');
}

if (!plugin.includes('public class EpicenterNativePlugin: CAPPlugin')) {
  fail('EpicenterNativePlugin.swift is not a CAPPlugin class');
}

if (!plugin.includes('public let jsName = "EpicenterNative"')) {
  fail('EpicenterNativePlugin.swift jsName is not EpicenterNative');
}

for (const method of requiredPluginMethods) {
  if (!plugin.includes(`CAPPluginMethod(name: "${method}"`) || !plugin.includes(`func ${method}(`)) {
    fail(`EpicenterNativePlugin.swift does not register/implement ${method}`);
  }
}

if (!infoPlist.includes('<key>UIBackgroundModes</key>') || !infoPlist.includes('<string>audio</string>')) {
  fail('Info.plist is missing UIBackgroundModes audio');
}

if (!process.exitCode) {
  console.log('✅ Official Capacitor iOS project is kept free of manual EpicenterNative source references.');
  console.log('✅ EpicenterNative is compiled through the local Capacitor/CocoaPods plugin package.');
  console.log('✅ TypeScript wrapper uses registerPlugin("EpicenterNative").');
  console.log('✅ Info.plist contains UIBackgroundModes audio.');
}
